#include <stdlib.h>

char	*ft_strdup(const char *str)
{
	int		i;
	char	*cpy;

	i = 0;
	while (str[i] != '\0')
		i++;
	if (!(cpy = malloc(i + 1 * sizeof(char))))
		return (0);
	i = 0;
	while (str[i] != '\0')
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
