#include <string.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned int		i;
	unsigned const char	*a;
	unsigned const char	*b;

	i = 0;
	a = (unsigned const char *)s1;
	b = (unsigned const char *)s2;
	while (i < n)
	{
		if (a[i] != b[i])
			return ((unsigned char)a[i] - (unsigned char)b[i]);
		i++;
	}
	return (0);
}
